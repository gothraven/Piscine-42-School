int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int cmpt;

	i = 0;
	cmpt = 0;
	while (tab[i])
	{
		if (f(tab[i]) == 1)
			cmpt++;
		i++;
	}
	return (cmpt);
}
