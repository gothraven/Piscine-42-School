char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char *s;
	char *d;

	s = src;
	d = dest;
	if (n != 0)
	{
		n++;
		while (--n != 0)
		{
			if ((*d++ = *s++) == 0)
			{
				while (--n != 0)
					*d++ = 0;
				break ;
			}
		}
	}
	return (dest);
}
